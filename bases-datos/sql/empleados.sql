SELECT AVG(SALARIO) AS 'SALARIO MEDIO' FROM EMPLEADOS WHERE DEPT_NO = '20';
SELECT COUNT(*) AS 'TOTAL EMPLEADOS' FROM EMPLEADOS;
SELECT COUNT(*) AS 'TOTAL EMPLEADOS CON COMISION' FROM EMPLEADOS WHERE COMISION > 0;
SELECT MAX(SALARIO) AS 'SALARIO MAXIMO' FROM EMPLEADOS;
SELECT MAX(APELLIDO) AS 'MAXIMO NOMBRE' FROM EMPLEADOS;
SELECT APELLIDO AS 'EMPLEADO CON MAYOR SALARIO' FROM EMPLEADOS ORDER BY SALARIO DESC LIMIT 1;
SELECT MIN(SALARIO) AS 'SALARIO MINIMO' FROM EMPLEADOS;
SELECT APELLIDO AS 'EMPLEADO CON MINIMO SALARIO' FROM EMPLEADOS ORDER BY SALARIO LIMIT 1;
SELECT SUM(SALARIO) AS 'SUMA SALARIOS' FROM EMPLEADOS;
SELECT SQRT(AVG(SALARIO * SALARIO) - AVG(SALARIO) * AVG(SALARIO)) AS 'DESVIACION ESTANDAR SALARIAL' FROM EMPLEADOS;
SELECT COUNT(DISTINCT DEPT_NO) AS 'NUMERO DEPARTAMENTOS' FROM EMPLEADOS;
SELECT DEPT_NO, COUNT(*) AS 'NUMERO PERSONAS' FROM EMPLEADOS GROUP BY DEPT_NO;
SELECT COUNT(*) AS 'NUMERO APPELIDOS EMPEZANDO CON A' FROM EMPLEADOS WHERE APELLIDO LIKE 'A%';
SELECT APELLIDO, SALARIO FROM EMPLEADOS WHERE APELLIDO LIKE 'A%' ORDER BY SALARIO DESC LIMIT 1;